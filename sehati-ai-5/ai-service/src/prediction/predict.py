import os
import joblib
import shap
import pandas as pd
import logging
from typing import Dict, Any

logger = logging.getLogger(__name__)

class ModelPredictor:
    def __init__(self, disease_name: str):
        self.disease_name = disease_name
        base_dir = os.path.dirname(os.path.dirname(os.path.dirname(__file__)))
        self.pipeline_path = os.path.join(base_dir, f'models/{disease_name}_pipeline.joblib')
        self.model_path = os.path.join(base_dir, f'models/{disease_name}_model.joblib')
        self.pipeline = None
        self.model = None
        
    def load_resources(self):
        if not os.path.exists(self.pipeline_path) or not os.path.exists(self.model_path):
            raise FileNotFoundError(f"Model or pipeline not found for {self.disease_name}. Run training first.")
        self.pipeline = joblib.load(self.pipeline_path)
        self.model = joblib.load(self.model_path)

    def predict(self, input_data: Dict[str, Any]) -> Dict[str, Any]:
        """
        Return:
        * risk_probability
        * risk_category
        * confidence_score
        * top_risk_factors
        """
        if not self.model:
            self.load_resources()
            
        df = pd.DataFrame([input_data])
        X_prep = self.pipeline.transform(df)
        
        prob = float(self.model.predict_proba(X_prep)[0][1])
        
        risk_category = "High" if prob >= 0.7 else "Moderate" if prob >= 0.4 else "Low"
        confidence_score = float(max(prob, 1 - prob))
        
        # 14. Generate SHAP explanations
        top_risk_factors = []
        try:
            # We use TreeExplainer for Random Forest/XGBoost, but generalize it
            explainer = shap.Explainer(self.model)
            shap_values = explainer(X_prep)

            # Extract exact feature names if generated by OneHotEncoder/ColumnTransformer
            feature_names = getattr(self.pipeline, 'get_feature_names_out', lambda: list(df.columns))()

            vals = shap_values.values[0]
            if len(vals.shape) > 1: # multi-class format for some reason
                vals = vals[:, 1]

            feature_importance = [(feature_names[i], abs(vals[i]), vals[i]) for i in range(len(vals))]
            feature_importance.sort(key=lambda x: x[1], reverse=True)
            
            # Map back clean names if they have prefixes
            clean_names = [f[0].split('__')[-1] for f in feature_importance[:3]]
            top_risk_factors = clean_names
        except Exception as e:
            logger.warning(f"Could not compute precise SHAP values online: {e}")
            top_risk_factors = list(df.columns)[:3] # Fallback to standard generic columns
            
        return {
            "risk_probability": round(prob, 4),
            "risk_category": risk_category,
            "confidence_score": round(confidence_score, 4),
            "top_risk_factors": top_risk_factors,
            "disclaimer": "AI predictions are intended for clinical decision support only."
        }

# Shared inference singletons
diabetes_predictor = ModelPredictor("diabetes")
heart_predictor = ModelPredictor("heart")
hypertension_predictor = ModelPredictor("hypertension")

def predict_diabetes(input_data: Dict[str, Any]):
    return diabetes_predictor.predict(input_data)

def predict_heart_disease(input_data: Dict[str, Any]):
    return heart_predictor.predict(input_data)

def predict_hypertension(input_data: Dict[str, Any]):
    return hypertension_predictor.predict(input_data)
